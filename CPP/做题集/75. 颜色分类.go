// 怎么仅使用常数空间的一趟扫描算法呢？
// 直观的
// 22222222222 先全变成2
// 11111122222 再变成1
// 00011122222 再变成0

func sortColors(nums []int)  {
    p0, p1 := 0, 0
    for idx, val := range nums {
        nums[idx] = 2
        if val <= 1 {
            nums[p1] = 1
            p1++
        } 
        if val == 0 {
            nums[p0] = 0
            p0++
        }
    }
}

// 示例 1：
// 输入：nums = [2,0,2,1,1,0]
// 输出：[0,0,1,1,2,2]


// 示例 2：
// 输入：nums = [2,0,1]
// 输出：[0,1,2]